// hangrix.runner.v1 is the wire contract the standalone `hangrix-runner`
// binary speaks with the platform. Replaces the ad-hoc JSON-over-HTTP
// `/api/runner/*` and `/api/runner/workflow-jobs/{id}/*` surface that
// lived in runner/handler/agent.go + workflow/handler/handler.go.
//
// Design notes:
//
//   - Each RPC has dedicated Request / Response messages (buf lint
//     STANDARD requirement). Empty responses are explicit messages so
//     future fields can be added without a service version bump.
//
//   - Tasks long-poll. Tasks() is unary with long-poll semantics: the
//     server blocks up to ~20s and returns either a populated Task or
//     an empty response (the 204 equivalent). The runner re-calls in a
//     loop. Server-streaming would push subsequent tasks before the
//     runner is ready to claim them; that's worse, not better, because
//     the runner processes one task at a time.
//
//   - JSON payload escape hatches. EnrollRequest.capabilities and
//     HeartbeatRequest.capabilities stay as `bytes` carrying raw JSON.
//     The platform never introspects this blob — it round-trips it
//     verbatim into runners.capabilities. Wrapping it in
//     google.protobuf.Struct would force a re-marshal every hop for no
//     gain.
//
//   - Token + auth. Enroll consumes a one-shot enroll token (`hgxe_`);
//     every other RPC carries the long-lived agent token (`hgxr_`) as
//     the bearer credential. The handler-side interceptor enforces this
//     split; the wire shape is identical otherwise.
//
//   - Binary downloads stay HTTP. `GET /api/runner/binaries/{name}`,
//     `GET /install/runner.sh`, and `GET /install/{asset}` are
//     plain-byte file serving — Connect would force a base64 round-trip
//     for the install-script `curl|sh` entrypoint, which is the whole
//     point of having that route. BootstrapPayload.binaries still
//     names them; they just don't go through this proto surface.

