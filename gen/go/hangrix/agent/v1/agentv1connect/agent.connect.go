// hangrix.agent.v1 is the wire contract the in-container agent speaks
// with the platform. Replaces the ad-hoc JSON-over-HTTP `/api/agent/
// sessions/{id}/*` surface that lived in agent_direct.go + http_transport.go.
//
// Design notes:
//
//   - Each RPC has dedicated Request / Response messages (buf lint
//     STANDARD requirement). The polymorphic event/control variants
//     are modelled as `oneof body` inside StreamInputsResponse.
//
//   - Streaming. StreamInputs is server-streaming: the server pushes
//     one StreamInputsResponse per platform-enqueued frame. The agent
//     closes the stream when it's ready to exit; the server closes
//     with OK when it terminates the wake (admin shutdown, repo
//     archive). Replaces the previous 20-s long-poll loop and its
//     5-minute client-side idle-grace timer.
//
//   - JSON payload escape hatches. EventFrame.payload, ToolCallFrame.
//     args, ToolCallFrame.result stay as `bytes` carrying raw JSON.
//     Event payloads are event-name-specific and change too often to
//     bind into proto; tool args/results are LLM-shaped JSON we never
//     introspect server-side. Wrapping them in google.protobuf.Struct
//     would force a full re-marshal at every hop for zero benefit.
//
//   - Session identity. Every request carries `session_id` even though
//     the bearer token already pins it. Server-side bearer middleware
//     enforces `request.session_id == token.session_id` so a leaked
//     token can't address a different session's queue.

